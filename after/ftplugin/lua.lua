-- These settings apply only to Lua buffers.

-- A <Tab> press will insert 4 spaces.
vim.opt_local.softtabstop = 4

-- Indentation commands like `>>` will indent by 4 spaces.
vim.opt_local.shiftwidth = 4

-- A literal tab character in the file is displayed as 4 spaces wide.
vim.opt_local.tabstop = 4

-- Use spaces instead of tab characters. This is essential for space-based indentation.
vim.opt_local.expandtab = true
